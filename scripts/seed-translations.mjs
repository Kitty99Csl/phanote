// Generates supabase/migrations/013_translations_seed.sql from src/lib/i18n.js
// Usage: node scripts/seed-translations.mjs (run from repo root)

import { i18n } from '../src/lib/i18n.js';
import { execSync } from 'child_process';
import { writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..');

let sha;
try {
  sha = execSync('git rev-parse --short HEAD', { cwd: repoRoot }).toString().trim();
} catch (e) {
  console.error('Failed to get git sha:', e.message);
  process.exit(1);
}

function usedIn(key) {
  if (key.startsWith('guide'))     return 'Guide';
  if (key.startsWith('statement')) return 'StatementScan';
  if (key.startsWith('onboard'))   return 'Onboarding';
  if (key.startsWith('budget'))    return 'Budget';
  if (key.startsWith('goal'))      return 'Goals';
  if (key.startsWith('ocr'))       return 'OCR';
  if (key.startsWith('wrap'))      return 'MonthlyWrap';
  if (key.startsWith('nav'))       return 'Nav';
  if (key.startsWith('home'))      return 'Home';
  if (key.startsWith('wallet'))    return 'Wallet';
  return null;
}

// Dollar-quote a string value. Safe because no i18n strings contain $$
function dq(s) {
  if (s === null || s === undefined) return 'NULL';
  return `$$${s}$$`;
}

// Collect rows from EN (source of truth); skip non-string values (months array)
const rows = [];
for (const [key, enVal] of Object.entries(i18n.en)) {
  if (typeof enVal !== 'string') continue;
  const loVal = typeof i18n.lo?.[key] === 'string' ? i18n.lo[key] : null;
  const thVal = typeof i18n.th?.[key] === 'string' ? i18n.th[key] : null;
  rows.push({ key, en: enVal, lo: loVal, th: thVal, used_in: usedIn(key) });
}

const count = rows.length;
const outPath = join(repoRoot, 'supabase/migrations/013_translations_seed.sql');

const valueLines = rows.map(({ key, en, lo, th, used_in }, i) => {
  const usedInSql = used_in ? `'${used_in}'` : 'NULL';
  const comma = i < count - 1 ? ',' : '';
  return `  (${dq(key)}, ${dq(en)}, ${dq(lo)}, ${dq(th)}, ${usedInSql})${comma}`;
});

const sql =
`-- Migration 013: seed translations from src/lib/i18n.js at commit ${sha}, ${count} keys
-- Generated by scripts/seed-translations.mjs — do not edit by hand
-- Re-run the script to regenerate after i18n.js changes
-- Additive: ON CONFLICT (code) DO NOTHING preserves admin edits (D19-Q2)

INSERT INTO public.translations (code, en, lo, th, used_in)
VALUES
${valueLines.join('\n')}
ON CONFLICT (code) DO NOTHING;

/* POSTFLIGHT VERIFICATION
   SELECT count(*) FROM translations;  -- expect ${count}
   SELECT code, en FROM translations WHERE code = $$welcome$$;
*/
`;

try {
  writeFileSync(outPath, sql, 'utf8');
  const thNulls = rows.filter(r => r.th === null).length;
  const loNulls = rows.filter(r => r.lo === null).length;
  console.log(`Written: ${outPath}`);
  console.log(`Keys seeded: ${count}`);
  console.log(`LO nulls: ${loNulls}`);
  console.log(`TH nulls: ${thNulls}`);
  if (thNulls > 0) {
    console.log('TH-null keys:', rows.filter(r => r.th === null).map(r => r.key).join(', '));
  }
} catch (e) {
  console.error('Failed to write file:', e.message);
  process.exit(1);
}
